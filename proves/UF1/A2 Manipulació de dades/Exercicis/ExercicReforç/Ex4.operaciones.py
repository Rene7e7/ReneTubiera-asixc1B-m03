"""
Exercici 4
Escriu el programa que calcula i mostra en pantalla la suma, resta, divisió i multiplicació de dos nombres entrats per l'usuari.

"""

num1 = int(input("Dame el primer numero: "))
num2 = int(input("Dame el segundo numero: "))

suma = num1 + num2
resta = num1 - num2
divisio = num1 / num2
multiplicacio = num1 * num2

print("El Resultat de la suma seria: ", suma)
print("El Resultat de la resta seria: ", resta)
print("El Resultat de la divisio seria: ", divisio)
print("El Resultat de la multiplicacio seria: ", multiplicacio)
